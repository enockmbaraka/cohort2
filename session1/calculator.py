#!/usr/bin/python3

# My custom calculator

def add(a, b):
    """This function returns the sum of a and b."""
    result = a + b
    return result

def sub(a, b):
    """This function returns the difference of a and b."""
    result = a - b
    return result

def mul(a, b):
    """This function returns the product of a and b"""
    result = a * b
    return result

def div(a, b):
    """This function returns the quotient of a and b"""
    result = a / b
    return result

def exp(a, b):
    """This function returns the exponent of a and b."""
    result = a ** b
    return result
